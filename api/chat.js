export default async function handler(req, res) {
  res.setHeader("Access-Control-Allow-Origin", "*");
  res.setHeader("Access-Control-Allow-Methods", "POST, OPTIONS");
  res.setHeader("Access-Control-Allow-Headers", "Content-Type");

  if (req.method === "OPTIONS") return res.status(200).end();
  if (req.method !== "POST") return res.status(405).json({ error: "Method not allowed" });

  try {
    const { message, context, history = [] } = req.body;
    if (!message?.trim()) return res.status(400).json({ error: "Message is required" });

    const anthropic = new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY });

    const messages = [
      ...history.slice(-8).map((m) => ({ role: m.role, content: m.content })),
      {
        role: "user",
        content: context
          ? `[Текущее состояние формы]\n${JSON.stringify(context, null, 2)}\n\n[Вопрос фаундера]\n${message}`
          : message,
      },
    ];

    const response = await anthropic.messages.create({
      model: "claude-sonnet-4-20250514",
      max_tokens: 500,
      system: SYSTEM_PROMPT,
      messages,
    });

    const reply = response.content
      .filter((b) => b.type === "text")
      .map((b) => b.text)
      .join("\n");

    res.json({ reply });
  } catch (err) {
    console.error("Chat error:", err);
    if (err.status === 401) return res.status(500).json({ error: "API key invalid" });
    res.status(500).json({ error: "Не удалось получить ответ. Попробуйте ещё раз." });
  }
}